''' exemplo dos calculos de angulo sen cos e tan'''
import math
ang = float(input('Digite o ângulo para ter o Coseno,  e Tan: '))
print('O Coseno de {} é {:.4f}'.format(ang, math.cos(ang)))
print('O seno do ângulo {} é {:.4f}'.format(ang, math.sin(ang)))
print('A tangente de {} é {:.4f}'.format(ang, math.tan(ang)))

